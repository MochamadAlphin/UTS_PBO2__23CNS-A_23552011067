public abstract class ItemMakanan {
    protected String nama;
    protected double harga;

    public ItemMakanan(String nama, double harga) {
        this.nama = nama;
        this.harga = harga;
    }

    public String getNama() {
        return nama;
    }

    public double getHarga() {
        return harga;
    }

    public abstract double hitungHarga();
}
